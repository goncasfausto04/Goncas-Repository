# Aula Teorica f-string e inputs

x = input("value:")
z = int(x)
y = z ** 2
print(f"The square root of {x} is {y}")

# The "f" before the string is used to create an f-string in Python.
# An f-string, or formatted string literal, is a way to embed expressions inside string literals,
# using curly braces {} to enclose the expressions
